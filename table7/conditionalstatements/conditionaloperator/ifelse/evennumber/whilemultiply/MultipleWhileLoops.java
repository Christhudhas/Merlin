public class MultipleWhileLoops {
    public static void main(String[] args) {
        int i = 1;
        System.out.println("First while loop (1 to 5):");
        while (i <= 5) {
            System.out.println(i);
            i++;
        }
         int j = 6;
        System.out.println("\nSecond while loop (6 to 10):");
        while (j <= 10) {
            System.out.println(j);
            j++;
        }
    }
}
